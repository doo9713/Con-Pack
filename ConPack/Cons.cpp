/* Game Back Ground */
void BackGround()
{
	CONPOS(0, 0) << "¦£";
	for (int i = 1; i < WINSIZEX / 2 - 1; ++i)
		CONPOS(i * 2, 0) << "¦¡";
	CONSOLE << "¦¤";
	for (int i = 0; i < WINSIZEY - 1; ++i)
	{
		CONPOS(0, i + 1) << "¦¢";
		CONPOS(WINSIZEX - 2, i + 1) << "¦¢";
	}
	CONPOS(0, WINSIZEY - 1) << "¦¦";
	for (int i = 1; i < WINSIZEX / 2 - 1; ++i)
		CONPOS(i * 2, WINSIZEY - 1) << "¦¡";
	CONSOLE << "¦¥";
}

